	private int rpno;
	private int mbno;
	private String pndelyn;
	private String pndate;
	private String pndatem;
	public int getPnno() {
		return pnno;
	}
	public void setPnno(int pnno) {
		this.pnno = pnno;
	}
	public int getRpno() {
		return rpno;
	}
	public void setRpno(int rpno) {
		this.rpno = rpno;
	}
	public int getMbno() {
		return mbno;
	}
	public void setMbno(int mbno) {
		this.mbno = mbno;
	}
	public String getPndelyn() {
		return pndelyn;
	}
	public void setPndelyn(String pndelyn) {
		this.pndelyn = pndelyn;
	}
